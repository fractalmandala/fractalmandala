	<h3 class="bord-bot p-bot-16">The Colonized Era</h3>
	<p>
		As I chronicle in <a href="https://www.brhat.in/dhiti/decolonizationapersonalfootprint" target="_blank" rel="noreferrer">this essay</a> I wrote, I've spent the majority of my life thoroughly colonized. Well into my journey back home- a ghar-vaapsi- now, even I look back at some of my old writings with embarassment.
	</p>
	<p>
		But available for reading they must remain, for they stand as a testimony of how blinded and ignorant deracination really makes us- and also as evidence that there is always time to wake up and return home.
	</p>
	<p>
		Most of the works here I would outright disown today, in that I no longer hold the beliefs they espouse. But one in particular, titled Peacock Feathers, I intend to "rewrite" on day soon as an intellectual inquiry into what I may write different- and what I may retain as is.
	</p>
